# ─────────────────────────────────────────────
#  Write output files
# ─────────────────────────────────────────────

def write_projects():
    os.makedirs(OUT_DIR, exist_ok=True)

    count = 0
    for p in PROJECTS:
        content = project(
            p["name"],
            p["description"],
            p["back_behaviors"],
            p["back_routes"],
            p["front_sections"],
            p["agent_behaviors"],
            p["features"],
            p["theme_lines"],
        )
        filename = f"{slug(p['name'])}_full_app.nc"
        path = os.path.join(OUT_DIR, filename)
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        count += 1
        print(f"  OK {filename}")

    # Generate variations (with/without auth, different themes, simplified)
    for p in PROJECTS[:10]:  # first 10 get variations
        # Variation: API-only (no frontend)
        simple_name = p["name"] + "-api"
        lines = [
            f"<|begin|>",
            f"// Description: {p['description']} (API only, no frontend)",
            f"// Type: service",
            f'service "{slug(simple_name)}"',
            f'version "1.0.0"',
            "",
        ]
        lines += p["back_behaviors"]
        lines += ["api:"] + [f"    {r}" for r in p["back_routes"]]
        lines += ["    GET /health runs health_check", ""]
        lines += ["to health check:", f'    respond with {{"status": "ok"}}']
        lines += ["<|end|>", ""]
        filename = f"{slug(p['name'])}_api_service.nc"
        path = os.path.join(OUT_DIR, filename)
        with open(path, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))
        count += 1
        print(f"  OK {filename}  (api-only variation)")

    print(f"\n  Total: {count} training files written to:\n  {OUT_DIR}\n")
    print("  Next step — train the model:")
    print(f"  nc train --data {OUT_DIR} --dim 512 --layers 8 --epochs 50")


if __name__ == "__main__":
    write_projects()
