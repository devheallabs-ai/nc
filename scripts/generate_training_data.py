#!/usr/bin/env python3
"""
NC Instruction-Tuning Training Data Generator
==============================================
Generates 250+ complete NC project files in instruction format.
Each file = one training example:
  <|begin|>
  // Description: [natural language prompt]
  // Type: full app
  service "[name]"
  ... complete nc-lang backend ...
  // === NC_FILE_SEPARATOR ===
  page "[Page Name]"
  ... complete nc-ui frontend ...
  // === NC_AGENT_SEPARATOR ===
  service "[name]-agent"
  ... nc-ai agent behaviors ...
  <|end|>

Usage:
  python3 generate_training_data.py
  # Writes to: nc-lang/engine/training_corpus/instruction-pairs/
"""

import os
import re

OUT_DIR = os.path.join(
    os.path.dirname(__file__),
    "..", "engine", "training_corpus", "instruction-pairs"
)


def slug(name):
    return re.sub(r"[^a-z0-9]+", "-", name.lower()).strip("-")


def title(name):
    return " ".join(w.capitalize() for w in name.replace("-", " ").split())


def backend(name, description, behaviors, routes, features=None):
    """Generate a nc-lang backend service."""
    feat = features or []
    lines = [
        f'<|begin|>',
        f'// Description: {description}',
        f'// Type: full app',
        f'service "{slug(name)}"',
        f'version "1.0.0"',
        '',
    ]
    if "auth" in feat:
        lines += [
            'middleware auth_check:',
            '    set token to request.headers.authorization',
            '    if token is empty:',
            '        respond with error "Unauthorized" status 401',
            '    set user to verify_token(token)',
            '    if user is empty:',
            '        respond with error "Invalid token" status 401',
            '',
        ]
    if "rate_limit" in feat:
        lines += [
            'configure:',
            '    rate_limit is 100',
            '    port is 8080',
            '',
        ]
    lines += behaviors
    lines += ['api:']
    lines += [f'    {r}' for r in routes]
    lines += ['    GET /health runs health_check', '']
    lines += [
        'to health check:',
        f'    respond with {{"status": "ok", "service": "{slug(name)}"}}',
    ]
    return '\n'.join(lines)


def frontend(name, page_title, sections, theme_lines=None):
    """Generate a nc-ui frontend page."""
    theme = theme_lines or [
        'theme:',
        '    primary is "#2563eb"',
        '    background is "#f8fafc"',
        '    font is "Inter, sans-serif"',
        '',
    ]
    lines = [
        f'page "{page_title}"',
        f'title "{page_title} | NC App"',
        '',
    ]
    lines += theme
    lines += sections
    return '\n'.join(lines)


def agent(name, description, behaviors=None):
    """Generate a nc-ai agent."""
    bh = behaviors or []
    lines = [
        f'// {title(name)} AI Agent',
        f'service "{slug(name)}-agent"',
        'version "1.0.0"',
        '',
        'configure:',
        '    max_tokens is 512',
        '    temperature is 0.7',
        '',
    ]
    lines += bh
    lines += [
        'to handle with prompt:',
        f'    purpose: "Handle user request for {name}"',
        f'    ask AI to "You are a helpful {name} assistant. {{{{prompt}}}}" save as response',
        '    respond with {"reply": response}',
        '',
        'to classify with input:',
        '    ask AI to "Classify as: create, read, update, delete, help. Input: {{input}}" save as intent',
        '    respond with {"intent": intent}',
        '',
        'api:',
        '    POST /agent          runs handle',
        '    POST /agent/classify  runs classify',
        '    GET  /agent/health    runs health_check',
        '',
        'to health check:',
        '    respond with {"status": "ok", "ai": "local"}',
    ]
    return '\n'.join(lines)


def project(name, description, back_behaviors, back_routes, front_sections, agent_behaviors=None, features=None, theme_lines=None):
    """Combine backend + frontend + agent into one training file."""
    back = backend(name, description, back_behaviors, back_routes, features)
    front = frontend(name, title(name), front_sections, theme_lines)
    agt = agent(name, description, agent_behaviors)
    return f"{back}\n\n// === NC_FILE_SEPARATOR ===\n\n{front}\n\n// === NC_AGENT_SEPARATOR ===\n\n{agt}\n<|end|>\n"


